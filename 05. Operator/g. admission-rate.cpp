/*
문제:
야구경기장의 좌석 수가 100개이고 입장객 수를 입력했을 때, 입장률을 출력하세요.

출력 예제:
전체 좌석은 100석입니다.
입장객 수를 입력하세요 90
입장률: 90.0%
*/

#include <stdio.h>
int main()
{
    int n;

    printf("전체 좌석은 100석입니다.\n");
    printf("입장객 수를 입력하세요 ");
    scanf("%d", &n);
    printf("입장률: %.1f%%", ((float)n / 100) * 100);

    return 0;
}