/*
문제:
배열 a의 첫 번째 요소를 가리키는 포인터 b를 선언하고 b를 통해 a[0]의 값과 주소를 출력하는 프로그램을 만드세요.

출력 예제:
a[0]의 주소값은 1492122280
a[0]의 주소값은 1492122280
a[0]의 주소값은 1492122280
a[0]에 저장된 값은 1
a[0]에 저장된 값은 1
*/

#include <stdio.h>
int main()
{
    int a[3] = {1};
    int *b;

    b = &a[0];

    printf("a[0]의 주소값은 %d\n", b);
    printf("a[0]의 주소값은 %d\n", b);
    printf("a[0]의 주소값은 %d\n", b);
    printf("a[0]에 저장된 값은 %d\n", *b);
    printf("a[0]에 저장된 값은 %d\n", *b);

    return 0;
    
}