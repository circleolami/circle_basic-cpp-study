/*
문제:
입력받은 수가 10 이상이면 10배 한 결과를 출력하세요
입력받은 수가 10 미만이면 아무런 연산도 하지 않습니다

출력 예제:
정수를 입력하세요 50
50을 10배한 결과는 500입니다.
프로그램을 마칩니다.
*/

#include <stdio.h>
int main()
{
    int n;

    printf("정수를 입력하세요 ");
    scanf("%d", &n);

    if (n >= 10)
        printf("%d를 10배 한 결과는 %d입니다.\n", n, n * 10);

    printf("프로그램을 마칩니다.\n");
    return 0;
}